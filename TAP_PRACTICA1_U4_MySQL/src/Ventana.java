    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        float precio = Float.valueOf(txtPre.getText());
        int existencia = Integer.parseInt(txtExi.getText());
        Productos productoInsertar = new Productos(txtDesc.getText(), precio, existencia);


        boolean resultadoInsert = mySQL.insertar(productoInsertar);
        if(resultadoInsert){
            txtDesc.setText("");
            txtPre.setText("");
            txtExi.setText("");
            actualizarJTable();
            JOptionPane.showMessageDialog(this,"EXITO! SE INSERTO");
        }else{
            JOptionPane.showMessageDialog(this,"ERROR! NO SE PUDO INSERTAR");
        }
    }//GEN-LAST:event_btnInsertarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int renglonSeleccionado = jTable1.getSelectedRow();
        if(renglonSeleccionado == -1){
           JOptionPane.showMessageDialog(this, "ERROR! DEBES SELECCIONAR UN RENGLÃN A BORRAR");
           return;
        }
        String idBorrar = tabla.getValueAt(renglonSeleccionado,0).toString();
        int respuesta = JOptionPane.showConfirmDialog(null,"Seguro que deseas borrar el renglÃ³n:"+ idBorrar+"?");
        if(respuesta == 0){
            boolean resultadoEliminar = mySQL.eliminar(idBorrar);
            if(resultadoEliminar){
                actualizarJTable();
                JOptionPane.showMessageDialog(this, "EXITO! SE BORRO");
            } 
        }else{
            return;
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

