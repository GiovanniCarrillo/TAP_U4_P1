    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        Productos prodActualizar = new Productos(txtD.getText(), Float.parseFloat(txtP.getText()), Integer.parseInt(txtE.getText()));
        prodActualizar.idProducto = Integer.parseInt(jTextField6.getText());
        int respuesta = JOptionPane.showConfirmDialog(null,"Seguro que deseas actualizar el producto:"+ prodActualizar.idProducto+"?");
        boolean respuestaActualizar = mySQL.actualizar(prodActualizar);
        if(respuesta == 0){
            if(respuestaActualizar){
                txtD.setText("");
                txtD.setText("");
                txtP.setText("");
                txtE.setText("");
                jTextField6.setText("");
                JOptionPane.showMessageDialog(this, "SE ACTUALIZO");
                actualizarJTable();
            }else{
                JOptionPane.showMessageDialog(this, "NO SE ACTUALIZO");
            }
        }
        jTextField6.setEditable(true);
    }//GEN-LAST:event_btnActualizarActionPerformed

